// Функция для определения, является ли сумма большой
export const isBigAmount = (amount: number, type: 'income' | 'expense', averageAmount: number): boolean => {
  // Если сумма в 2 раза больше средней для данного типа, считаем её большой
  return amount > averageAmount * 2;
};